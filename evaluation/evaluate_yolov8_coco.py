#!/usr/bin/env python3
"""
evaluate_yolov8_coco.py
=======================
Evaluate YOLOv8 checkpoints with COCOeval (pycocotools), using COCO-format
predictions generated by `model.val(..., save_json=True)` to match the
established notebook workflow and reduce runtime memory spikes.

Modes:
1) evaluate-runs: evaluate all selected runs on one dataset.
2) cross-subset: evaluate all selected runs across AGAR subset test sets.
"""

