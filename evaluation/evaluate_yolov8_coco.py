def require_file(path: Path, label: str) -> Path:
    if not path.exists() or not path.is_file():
        print(f"ERROR: {label} not found: {path}")
        sys.exit(1)
    return path


def require_dir(path: Path, label: str) -> Path:
    if not path.exists() or not path.is_dir():
        print(f"ERROR: {label} not found: {path}")
        sys.exit(1)
    return path


def resolve_subset_test_json(repro_splits: Path, subset: str) -> Path:
    candidates = [
        repro_splits / f"{subset}_test_coco.json",
        repro_splits / f"{subset}_test.json",
        repro_splits / f"test_{subset}100.json",
        repro_splits / f"test_annotated_{subset}100.json",
    ]
    for candidate in candidates:
        if candidate.exists() and candidate.is_file():
            return candidate
    print(f"ERROR: {subset} test json not found. Tried:")
    for candidate in candidates:
        print(f"  - {candidate}")
    sys.exit(1)


def parse_seed_filter(value: str | None) -> set[int] | None:
    if not value:
        return None
    out: set[int] = set()
    for part in value.split(","):
        p = part.strip()
        if not p:
            continue
        out.add(int(p))
    return out if out else None


def parse_subset_list(value: str) -> list[str]:
    token = value.strip().lower()
    if not token:
        print("ERROR: empty --subsets value")
        sys.exit(1)

    if token in SUBSET_GROUPS:
        return SUBSET_GROUPS[token]

    if "," not in token:
        if token in DEFAULT_SUBSETS:
            return [token]
        print(
            "ERROR: invalid --subsets value. "
            "Use one subset (bright/dark/vague/lowres), "
            "a group (agar/all), or a comma-separated list."
        )
        sys.exit(1)

    subsets = [x.strip().lower() for x in token.split(",") if x.strip()]
    invalid = [s for s in subsets if s not in DEFAULT_SUBSETS]
    if invalid:
        print(f"ERROR: invalid subset(s): {invalid}. Allowed: {', '.join(DEFAULT_SUBSETS)}")
        sys.exit(1)
    return subsets


def load_runtime_dependencies() -> tuple[Any, Any, Any]:
    try:
        from ultralytics import YOLO
        from pycocotools.coco import COCO
        from pycocotools.cocoeval import COCOeval
    except ImportError as exc:
        print("ERROR: ultralytics and pycocotools are required.")
        print("Install with: python -m pip install ultralytics pycocotools")
        print(f"Import failure: {exc}")
        sys.exit(1)
    return YOLO, COCO, COCOeval


def detect_seed_from_args_yaml(run_dir: Path) -> int | None:
    args_yaml = run_dir / "args.yaml"
    if not args_yaml.exists():
        return None
    try:
        text = args_yaml.read_text(encoding="utf-8", errors="ignore")
    except OSError:
        return None

    match = re.search(r"(?m)^seed:\s*([0-9]+)\s*$", text)
    if not match:
        return None
    try:
        return int(match.group(1))
    except ValueError:
        return None


def discover_runs(runs_root: Path, name_filter: str | None, seeds_filter: set[int] | None) -> list[dict[str, Any]]:
    runs: list[dict[str, Any]] = []
    for best_path in sorted(runs_root.rglob("weights/best.pt")):
        run_dir = best_path.parent.parent
        run_name = run_dir.name

        if name_filter and name_filter not in run_name:
            continue

        seed = None
        m = re.search(r"seed(\d+)", run_name.lower())
        if m:
            seed = int(m.group(1))
        else:
            seed = detect_seed_from_args_yaml(run_dir)
        if seeds_filter is not None and seed not in seeds_filter:
            continue

        runs.append(
            {
                "run_dir": run_dir,
                "run_name": run_name,
                "weights_path": best_path,
                "seed": seed,
            }
        )
    return runs


def normalize_label(name: str) -> str:
    return re.sub(r"[^a-z0-9]+", "", name.lower())


def load_data_yaml(data_yaml: Path) -> dict[str, Any]:
    import yaml

    with open(data_yaml, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f) or {}
    if not isinstance(data, dict):
        print(f"ERROR: invalid data.yaml format: {data_yaml}")
        sys.exit(1)
    return data


def parse_data_yaml_names(data: dict[str, Any]) -> list[str]:
    names = data.get("names")
    if isinstance(names, list):
        return [str(x) for x in names]
    if isinstance(names, dict):
        return [str(names[k]) for k in sorted(names.keys(), key=lambda x: int(x))]
    return []


def resolve_test_source(data_yaml: Path, data: dict[str, Any]) -> str:
    def _candidates_from_entry(entry: str) -> list[Path]:
        raw = Path(entry)
        out: list[Path] = []
        if raw.is_absolute():
            out.append(raw)
            return out

        # Standard relative-to-yaml location
        out.append(data_yaml.parent / raw)
        # As-written relative path (current working directory)
        out.append(raw)
        # Roboflow-style YAML sometimes stores "../test/images" while
        # data.yaml is already at dataset root; support by dropping leading "..".
        if raw.parts and raw.parts[0] == "..":
            out.append(data_yaml.parent / Path(*raw.parts[1:]))
        return out

    test_entry = data.get("test")
    if isinstance(test_entry, str) and test_entry.strip():
        candidates = _candidates_from_entry(test_entry.strip())
        for c in candidates:
            if c.exists():
                return str(c.resolve())
    if isinstance(test_entry, list) and test_entry:
        for entry in test_entry:
            for c in _candidates_from_entry(str(entry)):
                if c.exists():
                    return str(c.resolve())

    fallback_candidates = [
        data_yaml.parent / "images" / "test",
        data_yaml.parent / "test",
        data_yaml.parent / "test" / "images",
    ]
    for candidate in fallback_candidates:
        if candidate.exists():
            return str(candidate.resolve())

    print(f"ERROR: could not resolve test source from {data_yaml}")
    sys.exit(1)


def load_gt(gt_json: Path) -> dict[str, Any]:
    with open(gt_json, "r", encoding="utf-8") as f:
        return json.load(f)


def active_gt_categories(gt: dict[str, Any]) -> list[dict[str, Any]]:
    active_ids = {int(a["category_id"]) for a in gt.get("annotations", [])}
    categories = [c for c in gt.get("categories", []) if int(c["id"]) in active_ids]
    return sorted(categories, key=lambda c: int(c["id"]))


def all_gt_categories(gt: dict[str, Any]) -> list[dict[str, Any]]:
    return sorted(gt.get("categories", []), key=lambda c: int(c["id"]))


def build_image_lookup(gt: dict[str, Any]) -> tuple[dict[str, int], dict[str, int]]:
    by_exact: dict[str, int] = {}
    by_name: dict[str, int] = {}
    for img in gt.get("images", []):
        file_name = str(img.get("file_name", ""))
        image_id = int(img["id"])
        by_exact[file_name] = image_id
        by_name[Path(file_name).name] = image_id
    return by_exact, by_name


def remap_val_prediction_image_ids(
    preds: list[dict[str, Any]], gt: dict[str, Any]
) -> tuple[list[dict[str, Any]], int]:
    """
    Ultralytics val/save_json may emit string image IDs.
    Remap to integer COCO image IDs from GT using filename lookup.
    Returns (remapped_predictions, dropped_rows_count).
    """
    by_exact, by_name = build_image_lookup(gt)
    by_stem: dict[str, int] = {}
    for img in gt.get("images", []):
        fname = str(img.get("file_name", ""))
        by_stem[Path(fname).stem] = int(img["id"])

    gt_img_ids = {int(img["id"]) for img in gt.get("images", [])}
    remapped: list[dict[str, Any]] = []
    dropped = 0

    for pred in preds:
        row = dict(pred)
        image_id = row.get("image_id")
        mapped: int | None = None

        # Already valid int-like COCO image ID
        if isinstance(image_id, int) and image_id in gt_img_ids:
            mapped = int(image_id)
        elif isinstance(image_id, str):
            # numeric string
            if image_id.isdigit() and int(image_id) in gt_img_ids:
                mapped = int(image_id)
            else:
                # lookup by filename/token fields
                file_name = str(row.get("file_name", "")).strip()
                token = image_id.strip()
                candidates = [
                    file_name,
                    Path(file_name).name if file_name else "",
                    token,
                    f"{token}.jpg" if token and "." not in token else token,
                    Path(token).name if token else "",
                ]
                for c in candidates:
                    if not c:
                        continue
                    if c in by_exact:
                        mapped = by_exact[c]
                        break
                    name = Path(c).name
                    if name in by_name:
                        mapped = by_name[name]
                        break
                    stem = Path(name).stem
                    if stem in by_stem:
                        mapped = by_stem[stem]
                        break

        if mapped is None:
            dropped += 1
            continue

        row["image_id"] = int(mapped)
        # Keep JSON minimal and COCO-safe.
        row.pop("file_name", None)
        remapped.append(row)

    return remapped, dropped


def build_class_to_category_map(yolo_names: list[str], gt_categories: list[dict[str, Any]]) -> dict[int, int]:
    gt_by_norm = {normalize_label(str(c["name"])): int(c["id"]) for c in gt_categories}
    mapping: dict[int, int] = {}

    # Preferred: map by class name
    for idx, name in enumerate(yolo_names):
        cat_id = gt_by_norm.get(normalize_label(name))
        if cat_id is not None:
            mapping[idx] = cat_id

    if len(mapping) == len(yolo_names) and yolo_names:
        return mapping

    # Fallback: map by index to sorted active category IDs
    sorted_cat_ids = [int(c["id"]) for c in sorted(gt_categories, key=lambda c: int(c["id"]))]
    if len(sorted_cat_ids) >= len(yolo_names):
        mapping = {idx: sorted_cat_ids[idx] for idx in range(len(yolo_names))}
        return mapping

    print("ERROR: Could not build class->category mapping.")
    print(f"YOLO classes: {yolo_names}")
    print(f"GT categories: {[c.get('name') for c in gt_categories]}")
    sys.exit(1)


def maybe_fix_category_ids(preds: list[dict[str, Any]], valid_cat_ids: set[int]) -> tuple[list[dict[str, Any]], bool]:
    if not preds:
        return preds, False
    present = {int(p["category_id"]) for p in preds}
    if present.issubset(valid_cat_ids):
        return preds, False

    shifted = {cid - 1 for cid in present if cid > 0}
    if shifted and shifted.issubset(valid_cat_ids):
        for p in preds:
            if int(p["category_id"]) > 0:
                p["category_id"] = int(p["category_id"]) - 1
        return preds, True
    return preds, False


def yolo_predict_to_coco(
    *,
    model: Any,
    source: str,
    gt: dict[str, Any],
    class_to_cat: dict[int, int],
    conf: float,
    iou: float,
    imgsz: int,
    batch: int,
    max_det: int,
    device: str | None,
) -> list[dict[str, Any]]:
    by_exact, by_name = build_image_lookup(gt)
    preds: list[dict[str, Any]] = []

    results = model.predict(
        source=source,
        conf=conf,
        iou=iou,
        imgsz=imgsz,
        max_det=max_det,
        batch=batch,
        stream=True,
        save=False,
        verbose=False,
        device=device,
    )

    for r in results:
        img_path = Path(str(r.path))
        image_id = by_exact.get(img_path.name)
        if image_id is None:
            image_id = by_name.get(img_path.name)
        if image_id is None:
            continue

        boxes = r.boxes
        if boxes is None or len(boxes) == 0:
            continue

        xyxy = boxes.xyxy.cpu().numpy()
        confs = boxes.conf.cpu().numpy()
        classes = boxes.cls.cpu().numpy().astype(int)

        for box, score, cls_idx in zip(xyxy, confs, classes):
            cat_id = class_to_cat.get(int(cls_idx))
            if cat_id is None:
                continue
            x1, y1, x2, y2 = [float(v) for v in box.tolist()]
            w = max(0.0, x2 - x1)
            h = max(0.0, y2 - y1)
            preds.append(
                {
                    "image_id": int(image_id),
                    "category_id": int(cat_id),
                    "bbox": [x1, y1, w, h],
                    "score": float(score),
                }
            )
    return preds


def run_val_and_locate_predictions_json(
    *,
    model: Any,
    data_yaml: Path,
    eval_dir: Path,
    batch: int,
    imgsz: int,
    conf: float,
    iou: float,
    max_det: int,
    device: str | None,
    plots: bool,
) -> tuple[Any, Path]:
    """
    Run Ultralytics validation and return (results_obj, predictions_json_path).
    This follows the legacy notebook pattern:
      model.val(..., split='test', save_json=True, project=<eval_dir>, name='predictions')
    """
    val_results = model.val(
        data=str(data_yaml),
        split="test",
        batch=batch,
        imgsz=imgsz,
        conf=conf,
        iou=iou,
        max_det=max_det,
        plots=plots,
        save_json=True,
        project=str(eval_dir),
        name="predictions",
        exist_ok=True,
        device=device,
        verbose=False,
    )

    save_dir = Path(str(getattr(val_results, "save_dir", eval_dir / "predictions")))
    candidates = [
        save_dir / "predictions.json",
        eval_dir / "predictions" / "predictions.json",
    ]
    for c in candidates:
        if c.exists() and c.is_file():
            return val_results, c

    discovered = sorted(save_dir.rglob("predictions.json"))
    if discovered:
        return val_results, discovered[0]

    raise FileNotFoundError(
        f"predictions.json not found after model.val. "
        f"Checked: {[str(c) for c in candidates]} and recursive under {save_dir}"
    )


def run_cocoeval(
    *,
    COCO: Any,
    COCOeval: Any,
    gt_json: Path,
    pred_json: Path,
    cat_ids: list[int],
    max_det: int,
) -> tuple[dict[str, Any], str]:
    coco_gt = COCO(str(gt_json))
    coco_dt = coco_gt.loadRes(str(pred_json))
    coco_eval = COCOeval(coco_gt, coco_dt, "bbox")
    coco_eval.params.catIds = cat_ids
    coco_eval.params.maxDets = [1, 10, max_det]
    coco_eval.evaluate()
    coco_eval.accumulate()

    out = io.StringIO()
    with redirect_stdout(out):
        coco_eval.summarize()
    summary_text = out.getvalue()

    # Basic metrics (0-100 scale)
    stats = coco_eval.stats
    metrics: dict[str, Any] = {
        "AP": float(stats[0] * 100.0),
        "AP50": float(stats[1] * 100.0),
        "AP75": float(stats[2] * 100.0),
        "APs": float(stats[3] * 100.0),
        "APm": float(stats[4] * 100.0),
        "APl": float(stats[5] * 100.0),
        "AR100": float(stats[8] * 100.0),
    }

    # Per-class AP (mAP50-95)
    precision = coco_eval.eval["precision"]  # [TxRxKxAxM]
    for k_idx, cat_id in enumerate(cat_ids):
        p = precision[:, :, k_idx, 0, -1]
        p = p[p > -1]
        ap = float(p.mean() * 100.0) if p.size else float("nan")
        metrics[f"AP_catid_{cat_id}"] = ap

    return metrics, summary_text


def per_class_named_metrics(metrics: dict[str, Any], gt_categories: list[dict[str, Any]]) -> dict[str, Any]:
    out: dict[str, Any] = {}
    name_by_id = {int(c["id"]): str(c["name"]) for c in gt_categories}
    for key, value in metrics.items():
        if key.startswith("AP_catid_"):
            cat_id = int(key.split("_")[-1])
            name = name_by_id.get(cat_id, f"class_{cat_id}")
            safe_name = name.replace(".", "_").replace(" ", "_")
            out[f"AP_{safe_name}"] = value
    return out


def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    if not rows:
        return
    fields = list(rows[0].keys())
    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fields, restval="")
        writer.writeheader()
        writer.writerows(rows)


def evaluate_one(
    *,
    YOLO: Any,
    COCO: Any,
    COCOeval: Any,
    run: dict[str, Any],
    data_yaml: Path,
    gt_json: Path,
    subset_label: str,
    dataset_tag: str,
    report_rows: list[dict[str, Any]],
    args: argparse.Namespace,
    extra_fields: dict[str, Any] | None = None,
) -> dict[str, Any]:
    gt = load_gt(gt_json)
    gt_categories = active_gt_categories(gt)
    cat_ids = [int(c["id"]) for c in gt_categories]

    model = YOLO(str(run["weights_path"]))

    eval_dir = run["run_dir"] / f"eval_{subset_label}_cocoeval"
    eval_dir.mkdir(parents=True, exist_ok=True)
    pred_json = eval_dir / "predictions_coco.json"
    metrics_json = eval_dir / "metrics_cocoeval.json"
    summary_txt = eval_dir / "summary_cocoeval.txt"

    if not args.overwrite and pred_json.exists() and metrics_json.exists() and summary_txt.exists():
        with open(metrics_json, "r", encoding="utf-8") as f:
            cached = json.load(f)
        row = {
            "subset": subset_label,
            "dataset_tag": dataset_tag,
            "model_family": "yolov8",
            "backbone": run["run_name"],
            "run_dir": str(run["run_dir"]),
            "eval_dir": str(eval_dir),
            "threshold": args.conf_thres,
            "AP": cached.get("AP"),
            "AP50": cached.get("AP50"),
            "AP75": cached.get("AP75"),
            "APs": cached.get("APs"),
            "APm": cached.get("APm"),
            "APl": cached.get("APl"),
            "AR100": cached.get("AR100"),
            "pred_json_path": str(pred_json),
            "metrics_json_path": str(metrics_json),
            "summary_txt_path": str(summary_txt),
            "status": "ok_cached",
            "error_message": "",
            "timestamp_utc": datetime.now(timezone.utc).isoformat(),
            "seed": run["seed"],
            "ci_ready": 1,
            "category_fix_applied": cached.get("category_fix_applied"),
            "num_predictions": cached.get("num_predictions"),
            "speed_preprocess_ms": cached.get("speed_preprocess_ms"),
            "speed_inference_ms": cached.get("speed_inference_ms"),
            "speed_postprocess_ms": cached.get("speed_postprocess_ms"),
            "speed_total_ms": cached.get("speed_total_ms"),
        }
        if extra_fields:
            row.update(extra_fields)
        report_rows.append(row)
        return row

    try:
        val_results, val_pred_json = run_val_and_locate_predictions_json(
            model=model,
            data_yaml=data_yaml,
            eval_dir=eval_dir,
            batch=args.batch_size,
            imgsz=args.imgsz,
            conf=args.conf_thres,
            iou=args.iou_thres,
            max_det=args.max_det,
            device=args.device,
            plots=args.val_plots,
        )

        with open(val_pred_json, "r", encoding="utf-8") as f:
            preds = json.load(f)
        preds, dropped_unmapped = remap_val_prediction_image_ids(preds, gt)
        preds, fixed = maybe_fix_category_ids(preds, set(cat_ids))
        with open(pred_json, "w", encoding="utf-8") as f:
            json.dump(preds, f)

        metrics, summary = run_cocoeval(
            COCO=COCO,
            COCOeval=COCOeval,
            gt_json=gt_json,
            pred_json=pred_json,
            cat_ids=cat_ids,
            max_det=args.max_det,
        )
        metrics.update(per_class_named_metrics(metrics, gt_categories))
        metrics["category_fix_applied"] = fixed
        metrics["num_predictions"] = len(preds)
        metrics["num_predictions_dropped_unmapped_image"] = int(dropped_unmapped)
        speed = getattr(val_results, "speed", {}) or {}
        metrics["speed_preprocess_ms"] = float(speed.get("preprocess", 0.0) or 0.0)
        metrics["speed_inference_ms"] = float(speed.get("inference", 0.0) or 0.0)
        metrics["speed_postprocess_ms"] = float(speed.get("postprocess", 0.0) or 0.0)
        metrics["speed_total_ms"] = (
            metrics["speed_preprocess_ms"]
            + metrics["speed_inference_ms"]
            + metrics["speed_postprocess_ms"]
        )

        with open(metrics_json, "w", encoding="utf-8") as f:
            json.dump(metrics, f, indent=2)
        with open(summary_txt, "w", encoding="utf-8") as f:
            f.write(summary)

        row = {
            "subset": subset_label,
            "dataset_tag": dataset_tag,
            "model_family": "yolov8",
            "backbone": run["run_name"],
            "run_dir": str(run["run_dir"]),
            "eval_dir": str(eval_dir),
            "threshold": args.conf_thres,
            "AP": metrics.get("AP"),
            "AP50": metrics.get("AP50"),
            "AP75": metrics.get("AP75"),
            "APs": metrics.get("APs"),
            "APm": metrics.get("APm"),
            "APl": metrics.get("APl"),
            "AR100": metrics.get("AR100"),
            "pred_json_path": str(pred_json),
            "metrics_json_path": str(metrics_json),
            "summary_txt_path": str(summary_txt),
            "status": "ok",
            "error_message": "",
            "timestamp_utc": datetime.now(timezone.utc).isoformat(),
            "seed": run["seed"],
            "ci_ready": 1,
            "category_fix_applied": int(fixed),
            "num_predictions": len(preds),
            "num_predictions_dropped_unmapped_image": int(dropped_unmapped),
            "speed_preprocess_ms": metrics["speed_preprocess_ms"],
            "speed_inference_ms": metrics["speed_inference_ms"],
            "speed_postprocess_ms": metrics["speed_postprocess_ms"],
            "speed_total_ms": metrics["speed_total_ms"],
        }
        for k, v in metrics.items():
            if k.startswith("AP_"):
                row[k] = v
        if extra_fields:
            row.update(extra_fields)
        report_rows.append(row)
        print(
            f"[ok] {run['run_name']} | subset={subset_label} | "
            f"AP={row['AP']:.2f} AP50={row['AP50']:.2f} AP75={row['AP75']:.2f}"
        )
        return row
    except Exception as exc:
        row = {
            "subset": subset_label,
            "dataset_tag": dataset_tag,
            "model_family": "yolov8",
            "backbone": run["run_name"],
            "run_dir": str(run["run_dir"]),
            "eval_dir": str(eval_dir),
            "threshold": args.conf_thres,
            "status": "fail",
            "error_message": str(exc),
            "timestamp_utc": datetime.now(timezone.utc).isoformat(),
            "seed": run["seed"],
        }
        if extra_fields:
            row.update(extra_fields)
        report_rows.append(row)
        print(f"[error] {run['run_name']} | subset={subset_label} | {exc}")
        return row
    finally:
        # Release memory between runs to avoid Colab RAM growth.
        try:
            del model
        except Exception:
            pass
        try:
            import torch

            if torch.cuda.is_available():
                torch.cuda.empty_cache()
        except Exception:
            pass
        gc.collect()


def run_evaluate_runs(args: argparse.Namespace, YOLO: Any, COCO: Any, COCOeval: Any) -> None:
    if not args.data_yaml or not args.gt_json or not args.subset_label or not args.dataset_tag:
        print("ERROR: evaluate-runs mode requires --data-yaml --gt-json --subset-label --dataset-tag")
        sys.exit(1)

    data_yaml = require_file(args.data_yaml.resolve(), "data yaml")
    gt_json = require_file(args.gt_json.resolve(), "gt json")

    runs = discover_runs(
        runs_root=args.runs_root,
        name_filter=args.run_name_contains,
        seeds_filter=parse_seed_filter(args.seeds),
    )
    print(f"Found {len(runs)} YOLO runs under {args.runs_root}")
    if not runs:
        return

    rows: list[dict[str, Any]] = []
    for run in runs:
        evaluate_one(
            YOLO=YOLO,
            COCO=COCO,
            COCOeval=COCOeval,
            run=run,
            data_yaml=data_yaml,
            gt_json=gt_json,
            subset_label=args.subset_label,
            dataset_tag=args.dataset_tag,
            report_rows=rows,
            args=args,
        )

    out_csv = args.report_dir / f"yolo_cocoeval_{args.subset_label}.csv"
    write_csv(out_csv, rows)
    print(f"\nSaved: {out_csv}")


def run_cross_subset(args: argparse.Namespace, YOLO: Any, COCO: Any, COCOeval: Any) -> None:
    if not args.subsets_root or not args.repro_splits:
        print("ERROR: cross-subset mode requires --subsets-root and --repro-splits")
        sys.exit(1)

    subsets_root = require_dir(args.subsets_root.resolve(), "subsets root")
    repro_splits = require_dir(args.repro_splits.resolve(), "repro splits")
    subsets = parse_subset_list(args.subsets)

    runs = discover_runs(
        runs_root=args.runs_root,
        name_filter=args.run_name_contains,
        seeds_filter=parse_seed_filter(args.seeds),
    )
    print(f"Found {len(runs)} YOLO runs under {args.runs_root}")
    if not runs:
        return

    rows: list[dict[str, Any]] = []
    for run in runs:
        for subset in subsets:
            data_yaml = require_file(subsets_root / subset / "data.yaml", f"{subset} data yaml")
            gt_json = resolve_subset_test_json(repro_splits, subset)

            evaluate_one(
                YOLO=YOLO,
                COCO=COCO,
                COCOeval=COCOeval,
                run=run,
                data_yaml=data_yaml,
                gt_json=gt_json,
                subset_label=subset,
                dataset_tag=args.cross_dataset_tag,
                report_rows=rows,
                args=args,
                extra_fields={"trained_on": args.trained_on},
            )

    out_csv = args.report_dir / "yolo_cross_subset_cocoeval.csv"
    write_csv(out_csv, rows)
    print(f"\nSaved: {out_csv}")


def main() -> None:
    args = parse_args()
    args.runs_root = require_dir(args.runs_root.resolve(), "runs root")
    args.report_dir = args.report_dir.resolve()
    args.report_dir.mkdir(parents=True, exist_ok=True)

    YOLO, COCO, COCOeval = load_runtime_dependencies()

    if args.mode == "evaluate-runs":
        run_evaluate_runs(args, YOLO, COCO, COCOeval)
    else:
        run_cross_subset(args, YOLO, COCO, COCOeval)


if __name__ == "__main__":
    main()
